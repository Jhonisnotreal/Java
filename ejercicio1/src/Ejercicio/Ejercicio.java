package Ejercicio;

public class Ejercicio {
    public static void main(String[] args) {
        int numero = 12;
        System.out.println("El dato es un int: " + numero);

        long numero2 = 120432;
        System.out.println("El dato es un long: " + numero2);

        double numeroDecimal = 123.42d;
        System.out.println("El dato es un double: " +  numeroDecimal);

        String palabras = "Hola Planeta Tierra";
        System.out.println(("El dato es un String: " + palabras));

        boolean definicion = true;
        System.out.println("El dato es un boolenao: " + definicion);
    }
}
